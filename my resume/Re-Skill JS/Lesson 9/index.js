// Variables == Containers for storing data values.
// Let , Var , Const 
var a = 'Javascript';
console.log(a);    // Javascript 

let b = 2345;     // Using 'let' for block-scoped variables
console.log(b);   // 2345

const PI = 3.14;  
console.log(PI);   // 3.14


// Data types == Types of values that can be used in javascript , such as numbers , strings, booleans, objects, etc.
let num = 12;
let str = 'Javascript';
let bool = true;
let obj = {
    name: 'EcmaScript'
}
let arr = [10,20,'A','name'];

console.log(num);       // 12
console.log(str);       // Javascript
console.log(bool);      // true
console.log(obj);       // {name: 'EcmaScript}
console.log(arr);       // [10,20,'A','name']


